public class SumaParametros {
    public static void sumaParametros(int a, int b, int c) {
        int resultado = a + b + c;
        System.out.println ("El resultado obtenido es: " + resultado);
    }
    public static void main(String[] args) {
        int a = 80;
        int b = 50;
        int c = 1;

        sumaParametros (a, b, c); 
    }
}
